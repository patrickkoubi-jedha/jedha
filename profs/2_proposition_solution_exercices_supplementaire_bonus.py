# Exercice 14 – Analyse de texte
# Description : Demander une phrase à l’utilisateur, puis afficher nombre de mots, mot le plus long, fréquence des lettres.
# ============================================================
# Solution
texte = input("Entrez une phrase : ")

# Nombre de mots
mots = texte.split()
print("Nombre de mots :", len(mots))

# Mot le plus long
mot_plus_long = max(mots, key=len)
print("Mot le plus long :", mot_plus_long)

# Fréquence des lettres
frequences = {}
for lettre in texte:
    if lettre.isalpha():
        lettre = lettre.lower()
        frequences[lettre] = frequences.get(lettre, 0) + 1

print("Fréquence des lettres :")
for lettre, freq in sorted(frequences.items()):
    print(f"{lettre} : {freq}")