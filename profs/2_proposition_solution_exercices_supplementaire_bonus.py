# Exercice 9 – Compter les voyelles
# Description : Créer une fonction compte_voyelles(texte) qui retourne le nombre de voyelles dans une chaîne.
# ============================================================
# Solution
def compte_voyelles(texte):
    voyelles = "aeiouyAEIOUY"
    compteur = 0
    for lettre in texte:
        if lettre in voyelles:
            compteur += 1
    return compteur

# Exemple
print(compte_voyelles("Bonjour tout le monde"))


