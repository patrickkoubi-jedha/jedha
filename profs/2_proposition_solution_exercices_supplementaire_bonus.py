# Exercice 11 – Statistiques de notes
# Description : Créer un dictionnaire d’élèves avec leurs notes. Calculer la moyenne par élève et afficher l’élève avec la meilleure moyenne.
# ============================================================
# Solution
eleves = {
    "Alice": [12, 15, 14],
    "Bob": [9, 13, 11],
    "Clara": [15, 18, 17]
}

meilleure_moyenne = 0
meilleur_eleve = ""

for nom, notes in eleves.items():
    moyenne = sum(notes) / len(notes)
    print(f"{nom} a une moyenne de {moyenne:.2f}")
    if moyenne > meilleure_moyenne:
        meilleure_moyenne = moyenne
        meilleur_eleve = nom

print(f"Le meilleur élève est {meilleur_eleve} avec {meilleure_moyenne:.2f} de moyenne.")


